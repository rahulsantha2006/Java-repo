package com.rahul.core.examples1;


public class Example11_DoWhileloopInJava {

    public static void main(String args[]) {
        int a = 10;
        do {
            System.out.println(a);
            a--;
        } while (a > 1);


        int arr[] = {2, 54, 7, 96};
        int b = 0;
        do {
            System.out.println(arr[b]);
            b++;

        } while (b < 4);
    }
}
