package com.rahul.core.examples1;




public class Example10_WhileloopInJava {

    public static void main(String args[]) {
        int a=10;
        while(a>1){
            System.out.println(a);
            a--;
        }

        int arr[]={2,20,95,7};
        int b=0;
        while(b<4){
            System.out.println(arr[b]);
            b++;
        }
    }
}
